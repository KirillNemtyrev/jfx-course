package com.community.application.retrofit.response;

public class StockResponse {
    private Long cost;
    private float procent;
    private boolean upper;

    private Long much;

    public Long getCost() {
        return cost;
    }

    public float getProcent() {
        return procent;
    }

    public boolean isUpper() {
        return upper;
    }

    public Long getMuch() {
        return much;
    }
}
